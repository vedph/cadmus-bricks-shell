/**
 * Data of a Unicode emoji.
 */
export interface UnicodeEmoji {
  name: string;
  code: string;
  url: string;
}

/**
 * Service for looking up Unicode emojis by their name.
 */
@Injectable({
  providedIn: 'root',
})
export class EmojiService {
  private getEmoji(key: string): UnicodeEmoji {
    return {
      name: key,
      code: EMOJIS[key].replace('.png?v8', ''),
      url: `${EMOJI_URI}${EMOJIS[key]}`,
    };
  }

  /**
   *
   * @param name Any portion of the emoji name.
   * @param limit The maximum number of results to return.
   * @returns Array of matching emojis.
   */
  public lookupEmoji(name: string, limit = 10): UnicodeEmoji[] {
    name = name.toLowerCase();
    let matches: UnicodeEmoji[] = [];

    for (let key of Object.keys(EMOJIS)) {
      if (key.includes(name)) {
        matches.push(this.getEmoji(key));
        if (matches.length === limit) {
          break;
        }
      }
    }

    return matches;
  }
}
